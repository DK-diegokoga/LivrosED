package controller;

public class Listinha {
	    String nome;
	    String ano;
	    double preco;

	    public Listinha(String nome, String ano, double preco) {
	        this.nome = nome;
	        this.ano = ano;
	        this.preco = preco;
	    }
	    
	    public String getNome() {
	    	return nome;
	    }
	    
	    public String toString() {
	    	return ano;
	    }
	    
	    public double getPreco() {
	    	return preco;
	    }

}